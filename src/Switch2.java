public class Switch2 {
    public static void main(String[] args) {
        int k = 2;

        switch(k) {
            case 1:
                System.out.println("Krok 1");
            case 2:
                System.out.println("Krok 2");
            case 3:
                System.out.println("Krok 3");
            case 4:
                System.out.println("Krok 4");
        }
    }
}
