public class While {
    public static void main(String[] args) {
        int i = 100;
        while(i < 10) {
            System.out.println("Witam !!!");
            i++;
        }


        int j = 100;
        do {
            System.out.println("Witam !!!");
            j++;
        } while(j < 10);
    }
}
